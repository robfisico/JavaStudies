public class AreaCirculo {

	public static void main(String[] args) {
		//System.out.println(2+3);
		double raio = 3.4;
		final double pi = 3.14159;
		
		double area = pi * raio * raio;
		System.out.println("O valor da area de um circulo de raio " + raio + " e " + area + " m2");
	}
}
